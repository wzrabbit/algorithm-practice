L = int(input())

db = [
    [200, 210, 220],
    [210, 220, 225],
    [220, 225, 230],
    [225, 230, 235],
    [230, 235, 245],
    [235, 245, 250],
    [260, 265, 270],
    [265, 270, 275],
    [270, 275, 280],
    [275, 280, 285],
    [280, 285, 290],
    [285, 290, 295],
    [290, 295, 300],
]

for i in range(13):
    a, b, c = db[i]

    if L >= c:
        print(100, end=" ")
    elif L >= b:
        print(300, end=" ")
    elif L >= a:
        print(500, end=" ")
    else:
        print(0, end=" ")

    if i == 5:
        print()
